public class pokemon{
    
    
    // Aqui se guardara la informacion 
    private String nombre;
    private String tipo;
    private int vida;
    private int ataque;
    private int defensa;




    public pokemon(String nombre, String tipo, int vida, int ataque, int defensa){


        // this es el dedo que apunta a hacia una variable
        this.nombre = nombre; 
        this.tipo = tipo;
        this.vida = vida;
        this.ataque = ataque;
        this.defensa = defensa;


    }


    


}
